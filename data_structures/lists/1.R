### What are the six types of atomic vectors?
# character, integer, double, logical, raw, complex

### How does a list differ from an atomic vector?
# Both are 1-dimensional data structures, but an atomic vector must only contain elements of the same type
# whereas a list can contain elements of different types (even other lists).  This means lists can be recursive
# (containing themselves), whereas atomic vectors are not (atomic vectors cannot contain atomic vectors).
