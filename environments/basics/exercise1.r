### List three ways in which an environment differs from a list.

1.) It can't be compared with `==`, only `indentical` (though realistically, lists shouldn't be compared with `==` either)
2.) Lists, unlike environments, can be without names.
3.) The order of a list matters, unlike that of an environment.
4.) Environments have parents, lists do not. (So sad.)
5.) Environments have reference semantics, lists do not.
